        "class Spatial_Segmentation:\n",
        "    \n",
        "    \"\"\"\n",
        "    This class handles the process of dividing a region into subregions.\n",
        "    \n",
        "    MEMBER FUNCTIONS : regions\n",
        "    \"\"\"\n",
        "    def __init__(self, region, rows, cols):\n",
        "        \n",
        "        self.subregion_height = region.height / rows\n",
        "        self.subregion_width = region.width / cols\n",
        "        \n",
        "        point1, point2 = region.vertices\n",
        "        \n",
        "        self.origin = point1\n",
        "        self.end = point2\n",
        "        latitude1, longitude1 = (point1.latitude, point1.longitude)\n",
        "        latitude2, longitude2 = (point2.latitude, point2.longitude)\n",
        "        \n",
        "        subregions = []\n",
        "        \n",
        "        for i in range(rows):\n",
        "            row = []\n",
        "            begin_lat = latitude1 + self.subregion_height * i\n",
        "            end_lat = begin_lat + self.subregion_height\n",
        "            \n",
        "            for j in range(cols): \n",
        "                begin_long = longitude1 + self.subregion_width * j\n",
        "                end_long = begin_long + self.subregion_width\n",
        "                \n",
        "                vertices = (Point(begin_lat, begin_long), Point(end_lat, end_long))\n",
        "                subregion = Rectangular_Region(vertices)\n",
        "                \n",
        "                row.append(subregion)\n",
        "            \n",
        "            subregions.append(row)\n",
        "        \n",
        "        self.matrix = subregions\n",
        "        self.height = rows\n",
        "        self.width = cols\n",
        "        \n",
        "    def __iter__(self):\n",
        "        \n",
        "        for i in xrange(self.height):\n",
        "            for j in xrange(self.width):\n",
        "                yield self.matrix[i][j]\n",
        "                \n",
        "    def __getitem__(self, i):\n",
        "        return self.matrix[i]\n",
        "            \n",
        "    \n",
        "    def __str__(self):\n",
        "        \n",
        "        return_str = \"Listing {}*{} subregions:\\n\".format(self.height, self.width)\n",
        "        \n",
        "        for i in range(self.height):\n",
        "            for j in range(self.width):\n",
        "                return_str += str(self.matrix[i][j]) + \"\\n\"\n",
        " \n",
        "        return return_str\n",
        "\n",
        "    \n",
        "    def __len__(self):\n",
        "        \n",
        "        return len(self.matrix)\n",
        "                \n",
        "                \n",
