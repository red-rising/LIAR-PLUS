        "def time_from_string_usgs(time_str):\n",
        "    \n",
        "    tokens = time_str.split(\"T\")\n",
        "    \n",
        "    date = tokens[0]\n",
        "    time = tokens[1]\n",
        "    \n",
        "    tokens = date.split(\"-\")\n",
        "    year = int(tokens[0])\n",
        "    month = int(tokens[1])\n",
        "    day = int(tokens[2])\n",
        "    \n",
        "    tokens = time.split(\":\")\n",
        "    hour = int(tokens[0])\n",
        "    minute = int(tokens[1])\n",
        "    second = int(tokens[2][:2])\n",
        "    \n",
        "    return datetime(year, month, day, hour, minute, second)\n",
