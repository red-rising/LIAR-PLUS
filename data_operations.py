        "        \n",
        "        vertices = self.vertices\n",
        "        point1 = vertices[0]\n",
        "        point2 = vertices[1]\n",
        "        return_str = \"Region bounded between Latitudes ({}, {}); Longitudes ({}, {})\".format(point1.latitude, point2.latitude, point1.longitude, point2.longitude)\n",
