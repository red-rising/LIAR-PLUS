        "class Temporal_Segmentation:\n",
        "    \n",
        "    def __init__(self, time_window, num_segments):\n",
        "        \n",
        "        assert(isinstance(time_window, Interval))\n",
        "        \n",
        "        self.num_segments = num_segments\n",
        "        segment_length = (time_window.end - time_window.start) / num_segments\n",
        "        segments = []\n",
        "        \n",
        "        self.begin = time_window.start\n",
        "        self.end = time_window.end\n",
        "        \n",
        "        for i in range(num_segments):\n",
        "            \n",
        "            start = time_window.start + i * segment_length\n",
        "            end = start + segment_length\n",
        "            segment = Interval(start, end)\n",
        "            segments.append(segment)\n",
        "        \n",
        "        self.matrix = segments\n",
        "        self.segment_length = segment_length\n",
        "        \n",
        "    def __str__(self):\n",
        "        \n",
        "        return_str = \"\"\n",
        "\n",
        "        for segment in self.matrix:\n",
        "            return_str += str(segment) + \"\\n\"\n",
        "    \n",
        "        return return_str\n",
        "    \n",
        "    def __iter__(self):\n",
        "        \n",
        "        for i in xrange(self.num_segments):\n",
        "            yield self.matrix[i]\n",
        "    \n",
        "    def __getitem__(self, i):\n",
        "        \n",
        "        return self.matrix[i]\n",
        "    \n",
        "    def __len__(self):\n",
        "        return len(self.matrix)\n",
