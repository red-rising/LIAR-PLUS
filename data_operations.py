        "class Rectangular_Region:\n",
        "    \n",
        "    \"\"\"\n",
        "    MEMBER FIELDS:\n",
        "    vertices: a 2-tuple (Point, Point). \n",
        "    \n",
        "    NOTE : The latitude and longitude values at vertices[0] must be less than or equal to\n",
        "           those at vertices[1].\n",
        "    \"\"\"\n",
        "    \n",
        "    def __init__(self, vertices):\n",
        "        \n",
        "        point1 = vertices[0]\n",
        "        point2 = vertices[1]\n",
        "        \n",
        "        assert(point1.latitude < point2.latitude and point1.longitude < point2.longitude)\n",
        "        \n",
        "        \n",
        "        self.vertices = vertices\n",
        "        self.height = point2.latitude - point1.latitude\n",
        "        self.width = point2.longitude - point1.longitude\n",
        "        \n",
